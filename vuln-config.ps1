function Test-IsAdministrator {
    [CmdletBinding()]
    param()

    Write-Host "[*] Checking for administrative privileges..." -ForegroundColor Yellow

    $currentUser = [Security.Principal.WindowsIdentity]::GetCurrent()
    $principal = New-Object Security.Principal.WindowsPrincipal($currentUser)

    $isAdmin = $principal.IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)

    if (-not $isAdmin) {
        Write-Host "[-] Script must be run as Administrator." -ForegroundColor Red
    }

    Write-Host "[+] Running as Administrator." -ForegroundColor Green
}

function New-HexBlobToFile {
    param(
        [string]$HexBlob,
        [string]$FileName
    )

    # Echo hex blob into a variable
    $Hexfile = "file.hex"
    $HexBlob | Out-File -FilePath $HexFile

    # Decode hex and save as file
    certutil -f -decodeHex $HexFile $FileName 
    Remove-Item $Hexfile
}

function Confirm-MD5Hash {
    param(
        [string]$FileName,
        [string]$ExpectedHash
    )

    # Calculate MD5 hash of the file
    $md5 = (Get-FileHash -Path $FileName -Algorithm MD5 | Select-Object -ExpandProperty Hash).Tolower()
    $expected = $ExpectedHash.ToLower()
    # Compare calculated hash with the expected hash
    if ($md5 -eq $expected) {
        Write-Host "[+] Hash confirmed." -ForegroundColor Green
        return $true
    } else {
        Write-Host "[-] Hash mismatch. Manually Check.." -ForegroundColor Red
        return $false
    }
}

function Move-File {
    param(
        [string]$FileName,
        [string]$DestinationPath
    )

    if (-not (Test-Path $DestinationPath)) {
        New-Item -Path $DestinationPath -ItemType Directory | Out-Null
    }

    Move-Item -Path $FileName -Destination $DestinationPath -Force
}

