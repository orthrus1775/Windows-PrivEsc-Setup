function New-VulnerableService {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory = $true)]
        [string]$ServiceName,

        [Parameter(Mandatory = $true)]
        [string]$BinaryPath,

        [Parameter(Mandatory = $true)]
        [string]$DisplayName,

        [bool]$QuoteBinaryPath = $true
    )

    Write-Host "[*] Creating $ServiceName service.." -ForegroundColor Yellow
    Write-Host

    $serviceName = $ServiceName.ToLower()

    $existing = Get-Service -Name $ServiceName -ErrorAction SilentlyContinue
    if ($existing) {
        Write-Host "[*] Service $ServiceName already exists. Skipping create." -ForegroundColor Cyan
        return
    }

    if ($QuoteBinaryPath) {
        $batFile = [System.IO.Path]::GetTempFileName() -replace '\.tmp$', '.bat'
        'sc create {0} binpath= "\"{1}\"" type= own displayname= "{2}"' -f $ServiceName, $BinaryPath, $DisplayName |
            Out-File -FilePath $batFile -Encoding ascii
        cmd.exe /c $batFile | Out-Null
        Remove-Item -Path $batFile -Force
    } else {
        sc.exe create $ServiceName binpath= $BinaryPath type= own displayname= "$DisplayName" | Out-Null
    }
}

