function Set-LocalAdminAccount {
    [CmdletBinding()]
    param(
        [string]$Username = 'LocalSupport',
        [string]$Pass = 'P@ssw0rd123!'
    )

    Write-Host "[*] Ensuring local admin account '$Username' exists..." -ForegroundColor Yellow
    Write-Host

    $securePassword = ConvertTo-SecureString $Pass -AsPlainText -Force

    # Check if user exists
    $user = Get-LocalUser -Name $Username -ErrorAction SilentlyContinue

    if (-not $user) {
        Write-Host "[*] Creating local user $Username..." -ForegroundColor Cyan

        New-LocalUser `
            -Name $Username `
            -Password $securePassword `
            -FullName "IT Support Account" `
            -Description "Local IT repair/support account" `
            -PasswordNeverExpires `
            -AccountNeverExpires | Out-Null
    }
    else {
        Write-Host "[*] User $Username already exists, skipping creation." -ForegroundColor Cyan
    }

    # Add to Administrators group if not already
    $isAdmin = Get-LocalGroupMember -Group "Administrators" -ErrorAction SilentlyContinue |
               Where-Object { $_.Name -like "*\$Username" }

    if (-not $isAdmin) {
        Write-Host "[*] Adding $Username to Administrators group..." -ForegroundColor Cyan

        Add-LocalGroupMember -Group "Administrators" -Member $Username
    }
    else {
        Write-Host "[*] $Username is already a member of Administrators." -ForegroundColor Cyan
    }

    Write-Host "[+] Local admin account '$Username' ready." -ForegroundColor Green
}

